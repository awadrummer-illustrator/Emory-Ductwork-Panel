	bool IsPointNear(const DuctworkPoint& point, const std::vector<DuctworkPoint>& existing, double tolerance)
	{
		const double tolSq = tolerance * tolerance;
		for (size_t i = 0; i < existing.size(); ++i) {
			double dx = point.x - existing[i].x;
			double dy = point.y - existing[i].y;
			if ((dx * dx + dy * dy) <= tolSq) {
				return true;
			}
		}
		return false;
	}

	bool IsAutoGeneratedPartArt(AIArtHandle art)
	{
		if (!art) {
			return false;
		}

		const std::string note = DuctworkNotes::GetNote(art);
		if (note.find("MD:POINT_ROT=") != std::string::npos ||
			note.find("MDUX_RotationOverride") != std::string::npos ||
			note.find("MDUX_META") != std::string::npos) {
			return true;
		}

		double rotationValue = 0.0;
		return DuctworkMetadata::GetDouble(art, "MDUX_RotationOverride", rotationValue);
	}

	bool GetSinglePointAnchor(AIArtHandle art, DuctworkPoint& outPoint)
	{
		short type = 0;
		if (!art || sAIArt->GetArtType(art, &type) != kNoErr || type != kPathArt) {
			return false;
		}

		std::vector<DuctworkPoint> pathPoints;
		bool closed = false;
		if (!DuctworkGeometry::GetPathPoints(art, pathPoints, closed) || closed || pathPoints.size() != 1) {
			return false;
		}

		outPoint = pathPoints[0];
		return true;
	}

	bool GetPlacedCenter(AIArtHandle art, DuctworkPoint& outPoint)
	{
		short type = 0;
		if (!art || sAIArt->GetArtType(art, &type) != kNoErr || type != kPlacedArt) {
			return false;
		}

		AIRealRect bounds;
		if (sAIArt->GetArtBounds(art, &bounds) != kNoErr) {
			return false;
		}

		outPoint.x = (static_cast<double>(bounds.left) + static_cast<double>(bounds.right)) * 0.5;
		outPoint.y = (static_cast<double>(bounds.top) + static_cast<double>(bounds.bottom)) * 0.5;
		return true;
	}

	size_t RemoveAutoGeneratedPartsNearPoint(AILayerHandle layer, const DuctworkPoint& point, double tolerance)
	{
		if (!layer || !sAIArt) {
			return 0;
		}

		std::vector<AIArtHandle> art;
		DuctworkArt::CollectLayerArt(layer, art);
		std::vector<AIArtHandle> toDelete;
		const double toleranceSq = tolerance * tolerance;
		for (size_t i = 0; i < art.size(); ++i) {
			if (!IsAutoGeneratedPartArt(art[i])) {
				continue;
			}

			DuctworkPoint artPoint;
			if (!GetSinglePointAnchor(art[i], artPoint) && !GetPlacedCenter(art[i], artPoint)) {
				continue;
			}

			const double dx = artPoint.x - point.x;
			const double dy = artPoint.y - point.y;
			if ((dx * dx + dy * dy) <= toleranceSq) {
				toDelete.push_back(art[i]);
			}
		}

		for (size_t i = 0; i < toDelete.size(); ++i) {
			sAIArt->DisposeArt(toDelete[i]);
		}
		return toDelete.size();
	}

	bool EndpointIsConnected(int pathIndex, int endpointIndex, const std::vector<DuctworkConnection>& connections)
	{
		for (size_t i = 0; i < connections.size(); ++i) {
			const DuctworkConnection& conn = connections[i];
			if (conn.a == pathIndex && conn.endpointA == endpointIndex) {
				return true;
			}
			if (conn.b == pathIndex && conn.endpointB == endpointIndex) {
				return true;
			}
		}
		return false;
	}

	int CountEndpointToSegmentBranchChildren(int pathIndex, const std::vector<DuctworkConnection>& connections)
	{
		int count = 0;
		for (size_t i = 0; i < connections.size(); ++i) {
			const DuctworkConnection& conn = connections[i];
			if (conn.type != kConnectionEndpointToSegment) {
				continue;
			}

			if (conn.endpointA >= 0 && conn.segB >= 0 && conn.b == pathIndex) {
				++count;
				continue;
			}
			if (conn.endpointB >= 0 && conn.segA >= 0 && conn.a == pathIndex) {
				++count;
				continue;
			}
		}
		return count;
	}

	double ComputeAngleDegrees(const DuctworkPoint* prev, const DuctworkPoint& current, const DuctworkPoint* next)
	{
		double dx = 0.0;
		double dy = 0.0;
		if (prev) {
			dx = current.x - prev->x;
			dy = current.y - prev->y;
		} else if (next) {
			dx = next->x - current.x;
			dy = next->y - current.y;
		}
		if (std::fabs(dx) < 1e-6 && std::fabs(dy) < 1e-6) {
			return 0.0;
		}
		return std::atan2(dy, dx) * 180.0 / 3.14159265358979323846;
	}

	double NormalizeAngle(double angle)
	{
		angle = std::fmod(angle, 360.0);
		if (angle < 0.0) angle += 360.0;
		if (angle > 180.0) angle -= 360.0;
		if (angle > 90.0) angle -= 180.0;
		else if (angle < -90.0) angle += 180.0;
		return std::round(angle * 100.0) / 100.0;
	}

	double ComputeEndpointAngle(const DuctworkPath& path, size_t index)
	{
		if (path.points.size() < 2) {
			return 0.0;
		}
		if (index == 0) {
			const DuctworkPoint& a = path.points[0];
			const DuctworkPoint& b = path.points[1];
			return std::atan2(b.y - a.y, b.x - a.x) * 180.0 / 3.14159265358979323846;
		}
		const size_t last = path.points.size() - 1;
		const DuctworkPoint& a = path.points[last - 1];
		const DuctworkPoint& b = path.points[last];
		return std::atan2(b.y - a.y, b.x - a.x) * 180.0 / 3.14159265358979323846;
	}

